    public void atualizaTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tbl_tabelaCadastro.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();
        
        for(Produto p: pdao.read())
        {
            modelo.addRow(new Object[]
            {
                p.getCodigo(),
                p.getNome(),
                p.getQuantidade(),
                p.getVolume(),
                p.getPreço(),
            });
        }
        
    }
